package javaCurso2024;

public class While {
	public static void main(String[] args) {
		//criando variavel comecando com zero
		int contador = 0;
		while (contador <= 10) {
			System.out.print(contador + " " );
			//contador = contador +1; //incremente a variavel, aumenta o valor dela
			//contador +=1; // faz a mesma coisa em cima
			contador++; 
		}
	}

}
